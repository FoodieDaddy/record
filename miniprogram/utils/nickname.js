/**
 * 生成随机昵称
 * @returns {string}
 */
function generate() {
  const r = Math.random();
  let result;

  if (r < 0.35) {
    // 模式1: prefix + name (35%) → "雀神东方不败"
    result = pick(PREFIX) + pick(NAME);
  } else if (r < 0.55) {
    // 模式2: name + suffix (20%) → "东方不败驾到"
    result = pick(NAME) + pick(SUFFIX);
  } else if (r < 0.7) {
    // 模式3: prefix + name + 数字 (15%) → "十三幺令狐冲666"
    result = pick(PREFIX) + pick(NAME) + randNum();
  } else if (r < 0.82) {
    // 模式4: adj + name (12%) → "无敌的东方不败"
    result = pick(ADJ) + pick(NAME);
  } else if (r < 0.92) {
    // 模式5: prefix + name + suffix (10%) → "天胡金轮法王归来"
    result = pick(PREFIX) + pick(NAME) + pick(SUFFIX);
  } else {
    // 模式6: adj + prefix + name (8%) → "霸气的雀神令狐冲"
    result = pick(ADJ) + pick(PREFIX) + pick(NAME);
  }

  return result;
}

/**
 * 批量生成不重复昵称
 * @param {number} count
 * @returns {string[]}
 */
function generateBatch(count) {
  const set = new Set();
  let attempts = 0;
  while (set.size < count && attempts < count * 10) {
    set.add(generate());
    attempts++;
  }
  return Array.from(set);
}

function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function randNum() {
  const r = Math.random();
  if (r < 0.3) return String(Math.floor(Math.random() * 999) + 1);
  if (r < 0.5) return '';
  if (r < 0.7) return String(Math.floor(Math.random() * 99) + 1);
  return String(Math.floor(Math.random() * 9999) + 1);
}


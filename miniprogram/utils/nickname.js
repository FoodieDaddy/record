/**
 * 生成随机昵称 (纯净版)
 * @returns {string}
 */
function generate() {
  const r = Math.random();
  
  // 彻底废弃 3 词堆叠和数字拼接，只采用两两组合，最大字数恒定为 3 + 3 = 6
  if (r < 0.4) {
    // 模式1: prefix + name (40%) → 如 "杠开吗喽"
    return pick(PREFIX) + pick(NAME);
  } else if (r < 0.8) {
    // 模式2: name + suffix (40%) → 如 "细狗输麻了"
    return pick(NAME) + pick(SUFFIX);
  } else {
    // 模式3: adj + name (20%) → 如 "摆烂的赌王"
    return pick(ADJ) + pick(NAME);
  }
}

/**
 * 批量生成不重复昵称
 * @param {number} count
 * @returns {string[]}
 */
function generateBatch(count) {
  const set = new Set();
  let attempts = 0;
  // 容错率极高，不会出现因字数超限导致的死循环
  while (set.size < count && attempts < count * 10) {
    set.add(generate());
    attempts++;
  }
  return Array.from(set);
}

function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

